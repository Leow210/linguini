let i18nLocale = "en";

function setLocale(locale) {
  i18nLocale = I18N_LOCALES.includes(locale) ? locale : "en";
  document.documentElement.lang = i18nLocale;
}

function t(key, params) {
  let text = I18N[i18nLocale]?.[key] ?? I18N.en[key] ?? key;
  if (params) {
    for (const [name, value] of Object.entries(params)) {
      text = text.replaceAll(`{${name}}`, String(value));
    }
  }
  return text;
}

function levelLabel(value) {
  const key = LEVEL_KEY_BY_VALUE[value];
  return key ? t(key) : value;
}

function difficultyLabel(value) {
  const key = DIFFICULTY_KEY_BY_VALUE[value];
  return key ? t(key) : value;
}

function applyStaticTranslations() {
  for (const el of document.querySelectorAll("[data-i18n]")) {
    el.textContent = t(el.dataset.i18n);
  }
  for (const el of document.querySelectorAll("[data-i18n-html]")) {
    el.innerHTML = t(el.dataset.i18nHtml);
  }
  for (const el of document.querySelectorAll("[data-i18n-placeholder]")) {
    el.placeholder = t(el.dataset.i18nPlaceholder);
  }
  for (const el of document.querySelectorAll("[data-i18n-title]")) {
    el.title = t(el.dataset.i18nTitle);
  }
}
